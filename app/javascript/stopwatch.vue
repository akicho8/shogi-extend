          </div>`,
        confirmText: "わかった",
        canCancel: ["outside", "escape"],
        type: "is-info",
        hasIcon: true,
        onConfirm: () => { },
        onCancel:  () => { },
      })
    },

    log_display() {
      this.log_modal = this.$buefy.modal.open({
        parent: this,
        hasModalCard: true,
        component: {
          mounted() { },
          template: `
            <div class="modal-card is-size-7 stopwatch_log_dialog" style="width: auto">
